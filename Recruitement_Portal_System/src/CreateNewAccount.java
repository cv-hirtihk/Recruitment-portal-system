    private void jTextField8KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField8KeyReleased
        int PasswordCountForOne = 0 ;
        if(PasswordCountForOne > 30){
                jLabel16.setText("maximum of 30 Characters are allowed");
                jLabel16.setVisible(true);
            }if(evt.getKeyCode() == KeyEvent.VK_BACK_SPACE && !jTextField8.getText().isEmpty()){
                PasswordCountForOne -= 1;
                if(PasswordCountForOne >= 0){
                   checkForPasswordFieldOne[PasswordCountForOne] = '\0';
                }
            }else if(jTextField8.getText().isEmpty()){
                checkForPasswordFieldOne = new char[30];
                PasswordCountForOne = 0;
            }else{
                if(evt.getKeyCode() != KeyEvent.VK_SHIFT){
                   checkForPasswordFieldOne[PasswordCountForOne] = evt.getKeyChar();
                }
                PasswordCountForOne += 1;
            }
    }//GEN-LAST:event_jTextField8KeyReleased

