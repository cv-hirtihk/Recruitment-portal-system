    private boolean checkForEmpty(){
        int count = 0;
        if(jTextField1.getText().isEmpty()){
            jLabel1.setForeground(Color.red);
            count += 1;
        }if((int)jSpinner1.getValue() < 17){
            jLabel3.setForeground(Color.red);
            count += 1;
        }if(jRadioButton1.isSelected() == false && jRadioButton2.isSelected() == false){
            jLabel4.setForeground(Color.red);
            count += 1;
        }if(jTextField11.getText().isEmpty() || !jTextField11.getText().contains("-")){
            jLabel5.setForeground(Color.red);
            count += 1;
        }if(jTextField3.getText().isEmpty()){
            jLabel6.setForeground(Color.red);
            count += 1;
        }if(jComboBox1.getSelectedItem().equals("_______________________SELECT_DEPARTEMENT_______________________")){
            jLabel7.setForeground(Color.red);
            count += 1;
        }if(jRadioButton3.isSelected() == false && jRadioButton4.isSelected() == false){
            jLabel8.setForeground(Color.red);
            count += 1;
        }if(jTextField5.getText().isEmpty()){
            jLabel10.setForeground(Color.red);
            count += 1;
        }if(jTextField6.getText().trim().length() == 19 && jTextField6.getText().endsWith("@rajalakshmi.edu.in")){
            jLabel11.setForeground(Color.red);
            count += 1;
        }if(jTextField7.getText().isEmpty()){
            jLabel12.setForeground(Color.red);
            count += 1;
        }if(jTextField8.getText().isEmpty()){
            jLabel14.setForeground(Color.red);
            count += 1;
        }if(jTextField9.getText().isEmpty()){
            jLabel15.setForeground(Color.red);
            count += 1;
        }
        if(count != 0){
            jLabel16.setText("Fill all the field in red to continue");
            jLabel16.setVisible(true);
            return true;
        }
            return false;

    }
    
