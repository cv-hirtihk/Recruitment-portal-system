    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String firstName , lastName , Gender , collegeName , Departement , mailID , degreeType , passWord ;
        int Age , rollNumber , yearOfGraduation ;
        String DateOfBirth ;
        
        if(!checkForEmpty()){
            firstName = jTextField1.getText().trim();
            lastName = jTextField2.getText().isEmpty() ? "-" : jTextField2.getText().trim() ;
            Gender = jRadioButton1.isSelected() ? jRadioButton1.getText().trim() : jRadioButton2.getText().trim();
            DateOfBirth = jTextField11.getText().trim();
            collegeName = jTextField3.getText().trim();
            Departement = (String) jComboBox1.getSelectedItem(); 
            mailID = jTextField6.getText().trim();
            if(jTextField4.getText().isEmpty()){
                degreeType = jRadioButton3.isSelected() ? jRadioButton3.getText() : jRadioButton4.getText();
            }else{
                degreeType = jTextField4.getText().trim();
            }  
            if(String.valueOf(checkForPasswordFieldOne).equals(String.valueOf(checkForPasswordFieldTwo))){
                passWord = String.valueOf(jTextField9.getText());
            }else{
                passWord = "";
                jLabel16.setText("PassWord is not Matching");
                jLabel16.setVisible(true);
            }
            Age =  (int) jSpinner1.getModel().getValue(); 
            rollNumber = Integer.parseInt(jTextField7.getText().trim()); 
            yearOfGraduation = Integer.parseInt(jTextField5.getText().trim());

            DataBaseHandler Connecter = new DataBaseHandler();
            boolean Status = Connecter.UpdateNewAccountData(rollNumber , firstName , lastName , DateOfBirth , Age , Gender , collegeName , Departement , mailID , degreeType , yearOfGraduation , passWord);
            //System.out.println("firstname : " + firstName + " lastName : " + lastName +" Gender : "+ Gender +" DateOfBirth : " +DateOfBirth+" collegeName : " +collegeName+" Departement : "+Departement+" mailID : "+mailID+" degreeType : "+degreeType+" passWord : "+passWord+" Age : "+Age+ " rollNumber : "+rollNumber+" yearOfGraduation : " + yearOfGraduation);
            if(Status){
                this.setVisible(false);
                new pop_up_box().setStatement("Data uploaded to server Successfully!");
                new pop_up_box().setVisible(true);
            }
        }         
    }//GEN-LAST:event_jButton1ActionPerformed

